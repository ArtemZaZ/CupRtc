	Cs_Struct.GPIO_Pin = SPEEX_CS_GPIO_Pin_x;
	Cs_Struct.GPIO_Mode = SPEEX_CS_GPIO_Mode;
	Cs_Struct.GPIO_Speed = SPEEX_CS_GPIO_Speed;
	GPIO_Init(SPEEX_CS_GPIOx, &Cs_Struct);
	GPIO_SetBits(SPEEX_CS_GPIOx, SPEEX_CS_GPIO_Pin_x);
	
